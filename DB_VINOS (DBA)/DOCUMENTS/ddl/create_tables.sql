-- Tabla CLIENTE 
CREATE TABLE CLIENTE ( 
  CODIGO_CLI NUMBER(10) NOT NULL PRIMARY KEY, 
  CATEGORIA VARCHAR2(20) NOT NULL, 
  NOM_RAZON VARCHAR2(50) NOT NULL, 
